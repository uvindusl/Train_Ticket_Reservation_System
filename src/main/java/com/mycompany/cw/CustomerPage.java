    private void btnselectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnselectActionPerformed
    String start = String.valueOf(combostart.getSelectedItem());
    String end = String.valueOf(comboend.getSelectedItem());
    
    if(combostart.getSelectedItem() == null)
    {
        JOptionPane.showMessageDialog(null, "Error: Please Select Depature Station", "Error", JOptionPane.ERROR_MESSAGE);
    }else if(comboend.getSelectedItem() == null){
        JOptionPane.showMessageDialog(null, "Error: Please Select Depature Station", "Error", JOptionPane.ERROR_MESSAGE);
    }else{
        try {
        String sqlquery = "SELECT Trainid, Tname, start, end FROM train WHERE start = ? AND end = ?";
        pst = conn.prepareStatement(sqlquery);
        pst.setString(1, start);
        pst.setString(2, end);
        rs = pst.executeQuery();

        DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
        
        if (rs.next()) {
            String Trainid = rs.getString("Trainid");
            String Tname = rs.getString("Tname");
            String from = rs.getString("start"); 
            String to = rs.getString("end");          
            tblModel.setRowCount(0);
            String tbData[] = {Trainid, Tname, from, to};
            tblModel.addRow(tbData);
            
        }else{
            JOptionPane.showMessageDialog(null,"Train are not available now");
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, e);
    }
    }
    
    

    }//GEN-LAST:event_btnselectActionPerformed

