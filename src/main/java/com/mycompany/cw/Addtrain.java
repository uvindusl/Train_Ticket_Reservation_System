    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String tname = txtname.getText();
        String start = String.valueOf(combofrom.getSelectedItem());
        String end = String.valueOf(comboto.getSelectedItem());
        try{
                pst = conn.prepareStatement("INSERT INTO train (Tname,start,end) VALUES ( ?, ?, ?)");
                pst.setString(1, tname);
                pst.setString(2, start);
                pst.setString(3,end);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Successfully ADD");
                
                this.txtname.setText("");
                this.combofrom.setSelectedIndex(-1);
                this.comboto.setSelectedIndex(-1);
            }catch(SQLException e){
                JOptionPane.showMessageDialog(null,e);

            }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
         try {
        String sqlquery = "SELECT Trainid,Tname,start,end FROM train";
        pst = conn.prepareStatement(sqlquery);
        rs = pst.executeQuery();

        DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
        tblModel.setRowCount(0);
        while (rs.next()) {
            String tid = rs.getString("Trainid");
            String tname = rs.getString("Tname");
            String start = rs.getString("start"); // Adjust this if Phoneno is stored as a string
            String end = rs.getString("end");
            
            

            String tbData[] = {tid, tname, start, end};
            tblModel.addRow(tbData);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, e);
    }
    }//GEN-LAST:event_formWindowActivated

