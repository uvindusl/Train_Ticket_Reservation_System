    private void btnselectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnselectActionPerformed
        // TODO add your handling code here:
        String start = String.valueOf(combostart.getSelectedItem());
        String end = String.valueOf(comboend.getSelectedItem());
        
        try{
                String sqlquery = "Select Tid , price From ticket Where start=? AND end=?";
                pst = conn.prepareStatement(sqlquery);
                pst.setString(1, start);
                pst.setString(2, end);
                rs=pst.executeQuery();
                if(rs.next()){
                    txtticket.setText(rs.getString("Tid"));
                    txtprice.setText(rs.getString("price"));
                    calculation();
                    
                    try{
                        String sqlquery1 = "Select Bid From booking";
                        PreparedStatement pst2 = conn.prepareStatement(sqlquery1);
                        ResultSet rs2 = pst2.executeQuery();
            
                        while(rs2.next()) {
                         int bookid = Integer.parseInt(rs2.getString("Bid"));
                         bookid = bookid +1;
                         txtbookid.setText(String.valueOf(bookid));
                        }
                        rs2.close();
                        pst2.close();
                        
                    }catch(SQLException e){
                        JOptionPane.showMessageDialog(null, e);
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "Sorry Tickets are not available");
                    
                }
            }catch(SQLException e){
                JOptionPane.showMessageDialog(null,e);
            }
        
        
    }//GEN-LAST:event_btnselectActionPerformed

