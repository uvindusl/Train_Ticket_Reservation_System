    private void btninvoiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninvoiceActionPerformed
        // TODO add your handling code here:
        int Bid = Integer.valueOf(txtbookid.getText());
        String Cname = txtcustomerna.getText();
        String tid = txtticket.getText();
        String start = String.valueOf(combostart.getSelectedItem());
        String end = String.valueOf(comboend.getSelectedItem());
        int seats = Integer.valueOf(spinseates.getValue().toString());
        String Date = txtdate.getDate().toString();
        String total = txttotal.getText();
        try{
            String sqlquery = "INSERT INTO booking (Bid,Cname,tid,start,end,seats,Date_Time,total) VALUES (?,?,?,?,?,?,?,?)";
            pst = conn.prepareStatement(sqlquery);
            pst.setInt(1, Bid);
            pst.setString(2, Cname);
            pst.setString(3,tid);
            pst.setString(4,start);
            pst.setString(5, end);
            pst.setInt(6, seats);
            pst.setString(7, Date);
            pst.setString(8,total);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Successfuly booked the ticket");
            
            
            String sqlquery1 = "INSERT INTO suborder (Bid) VALUES (?)";
            PreparedStatement pst2 = conn.prepareStatement(sqlquery1);
            pst2.setInt(1, Bid);
            pst2.executeUpdate();
                           
            this.txtbookid.setText("");
            this.txtticket.setText("");
            this.txtprice.setText("");
            this.txttotal.setText("");
            combostart.setSelectedIndex(-1); // Clear the selection
            comboend.setSelectedIndex(-1); // Clear the selection
            spinseates.setValue(1); // This sets the spinner to the value 1
            txtdate.setDate(null);
            
            
            
           
            
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_btninvoiceActionPerformed

