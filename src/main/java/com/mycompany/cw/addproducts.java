    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String tid = txttid.getText();
        String start = String.valueOf(combostart.getSelectedItem());
        String end = String.valueOf(comboend.getSelectedItem());
        double price = Double.parseDouble(txtprice.getText());
        if(start == end){
            JOptionPane.showMessageDialog(null, "Start and End Destinations can't be the same", "Error", JOptionPane.ERROR_MESSAGE);
            this.txttid.setText("");
            this.txtprice.setText("");
            this.combostart.setSelectedIndex(-1);
            this.comboend.setSelectedIndex(-1);

            
            
        }else{
          
            
            try{
                pst = conn.prepareStatement("INSERT INTO ticket (Tid,start,end,price) VALUES (?, ?, ?, ?)");
                pst.setString(1, tid);
                pst.setString(2, start);
                pst.setString(3,end);
                pst.setDouble(4, price);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Successfully ADD");
                
                this.txttid.setText("");
                this.txtprice.setText("");
                this.combostart.setSelectedIndex(-1);
                this.comboend.setSelectedIndex(-1);

         

            }catch(SQLException e){
                JOptionPane.showMessageDialog(null,e);

            }
        }
            
       
    }//GEN-LAST:event_jButton1ActionPerformed

